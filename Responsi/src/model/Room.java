package model;

/**
 *
 * @author Lenovo
 */
public class Room {
    private String name;
    private String size;
    private int price;
    private String status;

    public Room(String name, String size, int price, String status) {
        this.name = name;
        this.size = size;
        this.price = price;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
